# Variabel, suatu 'wadah' yang akan menyimpan nilai 
# berupa angka / huruf / boolean

# Assignment variabel
a = 10 # Variabel 'a' bernilai 10
print("a =", a)

# Assignment indirect
b = a + 20
print("b = a + 20 =", b)

