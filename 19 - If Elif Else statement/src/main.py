# percabangan if, elif, else

nama = input('Siapa nama kamu? : ')

# Program percabangan
if nama == 'maulana':
    # maulana sudah dikenali
    print(f"Hai {nama}, hari yang cerah")
elif nama == 'aji':
    # aji sudah dikenali
    print(f"Hai {nama}, jangan lupa belajar")
else:
    # belum dikenali, maka akan diucapkan salam kenal
    print(f"Salam kenal {nama}")

