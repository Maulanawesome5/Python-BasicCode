# Tipe data collection List, Tuple, Set, Dictionary

# Tuple, tidak ada method untuk mengubah-menambah-menghapus
data_tuple = (7, 8, 9, 10)
print(f"Data Tuple = {type(data_tuple)}")
print(f"Data Tuple = {data_tuple}")
print(f"Data Tuple[3] = {data_tuple[3]}")

print("\n")

# Set, tidak support index
data_set = {10, 4, 3, 2, 4, 7, 6, 5}
print(f"Data Set = {type(data_set)}")
print(f"Data Set = {data_set}")

