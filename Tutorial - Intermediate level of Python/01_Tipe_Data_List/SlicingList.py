# List : Ordered, mutable, allows duplicate number

my_list = [1, 2, 3, 4, 5, 6, 7, 8, 9]
print(my_list)

# mekanisme slicing atau irisan pada list
irisan = my_list[1:]  # start_index
print(irisan)

irisan = my_list[1:5]  # start_index : stop_index
print(irisan)

irisan = my_list[::2]  # start_index : stop_index : step
print(irisan)
